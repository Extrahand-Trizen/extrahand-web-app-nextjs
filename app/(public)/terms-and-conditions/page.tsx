function renderBodyLines(lines: string[]) {
  const renderedLines = [];
  let index = 0;

  while (index < lines.length) {
    if (isCustomerCancellationTableStart(lines, index)) {
      renderedLines.push(renderTable(CUSTOMER_CANCELLATION_TABLE, `table-${index}`));
      index += customerCancellationTableLines;
      continue;
    }

    if (isTaskerCancellationTableStart(lines, index)) {
      renderedLines.push(renderTable(TASKER_CANCELLATION_TABLE, `table-${index}`));
      index += taskerCancellationTableLines;
      continue;
    }

    renderedLines.push(renderLegalLine(lines[index], index));
    index += 1;
  }

  return renderedLines;
}

