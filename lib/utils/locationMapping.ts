/**
 * Convert a location name/address to coordinates
 * Falls back to default Hyderabad coordinates if not found
 */
export function getLocationCoordinates(
  location: string,
  defaultCoordinates = { lat: 17.385, lng: 78.4867 }
): { lat: number; lng: number } {
  if (!location) return defaultCoordinates;

  const normalized = location.toLowerCase().trim();

  // Try exact match first
  if (LOCATION_COORDINATES[normalized]) {
    return LOCATION_COORDINATES[normalized];
  }

  // Try partial matches
  for (const [key, coords] of Object.entries(LOCATION_COORDINATES)) {
    if (normalized.includes(key) || key.includes(normalized)) {
      return coords;
    }
  }

  // If no match found, return default
  return defaultCoordinates;
}

/**
 * Format a location for display
 */
export function formatLocation(address?: string, city?: string): string {
  if (address) return address;
  if (city) return city;
  return "Hyderabad";
}

/**
 * Get all available locations for filtering
 */
export function getAvailableLocations(): string[] {
  const hyderabadScopedKeys = Object.keys(LOCATION_COORDINATES).filter((loc) => {
    const normalized = loc.toLowerCase();
    return (
      normalized.includes("hyderabad") ||
      normalized.includes("secunderabad") ||
      normalized === "hyderabad" ||
      normalized === "secunderabad"
    );
  });

  const formattedMap = new Map<string, string>();

  hyderabadScopedKeys.forEach((loc) => {
    const parts = loc.split(",").map((part) => part.trim());
    const base = parts[0];
    const city = parts[1];

    const titleCase = (value: string) =>
      value
        .split(" ")
        .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
        .join(" ");

    const baseTitleCase = titleCase(base);

    if (city) {
      formattedMap.set(baseTitleCase, `${baseTitleCase}, ${titleCase(city)}`);
    } else {
      if (!formattedMap.has(baseTitleCase)) {
        formattedMap.set(baseTitleCase, baseTitleCase);
      }
    }
  });

  return Array.from(formattedMap.values()).sort((a, b) => a.localeCompare(b));
}
